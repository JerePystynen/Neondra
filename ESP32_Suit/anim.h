class Anim {
public:
  Anim(
    // Each frame has data for all of the helmet matrix pixels.
    // All helmet matrix pixels are set inside 1 array.
    // Total amount of WS2812B LEDs is PIXEL_COUNT.
    uint8_t frames[][PIXEL_COUNT],
    uint8_t frame_count,
    uint8_t duration_ms
  );
private:
  uint8_t (*frames)[PIXEL_COUNT];  // Pointer to arrays of size PIXEL_COUNT
  static uint8_t _frame_count;
  static uint8_t _duration_ms;
};

Anim::Anim(uint8_t frame_count, uint8_t frames[][PIXEL_COUNT], uint8_t duration_ms) {
  _frameCount = frame_count;
  _duration_ms = duration_ms;
  frames = new uint8_t[frame_count][PIXEL_COUNT];
  // For each frame.
  for (int f = 0; f < _frame_count; ++f) {
    // For each pixel.
    for (int p = 0; p < PIXEL_COUNT; ++p) {
      frames[f][p] = frames[f][p];
    }
  }
}

